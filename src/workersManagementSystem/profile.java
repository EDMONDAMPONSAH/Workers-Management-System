    public void loadData() {

        try {
            con = database.connecDB();
            pst = con.prepareStatement("SELECT * FROM admin where email=?");
            pst.setString(1, emaill);
            rs = pst.executeQuery();
            if (rs.next()) {

                jTextField4.setText(rs.getString("name"));
                jTextField2.setText(rs.getString("phone"));
                jTextField6.setText(rs.getString("status"));
                jPasswordField1.setText(rs.getString("password"));
                
                if (rs.getString("gender").equals("Male")) {
                    jRadioButton1.setSelected(true);
                } else {
                    jRadioButton2.setSelected(true);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

