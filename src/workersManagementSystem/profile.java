    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField4.getText();
        String phone = jTextField2.getText();
        String pass = jPasswordField1.getText();
        String status = jTextField6.getText();

        try {
            con = database.connecDB();
            pst = con.prepareStatement("UPDATE admin set name=?,phone=?,password=?,gender=?,status=? where email=?");
            pst.setString(1, name);
            pst.setString(2, phone);
            pst.setString(3, pass);
            if(jRadioButton1.isSelected()){
                pst.setString(4, "Male");
            }else if(jRadioButton2.isSelected()){
                pst.setString(4, "Female");
            }
            pst.setString(5, status);
            
            pst.setString(6, emaill);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Profile updated successfully");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

